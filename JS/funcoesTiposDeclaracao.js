//function declaration
    /*função é pré carregada e pode ser utilizada antes da declaração */
function soma(x, y){
    return x + y
}

//function expression
    /* só pode ser utilizada após sua declaração */
const sub = function (x, y){
    return x-y
}
 //named function expression
    /* só pode ser utilizada após sua declaração */
    /* pouco utilizada */
 const mult = function mult (x, y ){
     return x * y
 }

